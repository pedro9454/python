n1 = input('Digite um número: ')
print('Só tem espaço?', n1.isspace())
print('É um número? ', n1.isnumeric())
print('É alfabetico? ', n1.isalpha())
print('É alfanúmerico? ', n1.isalnum())
print('Está só em maiúscula? ', n1.isupper())
print('Está só em minúscula? ', n1.islower())
print('Está captalizada? ', n1.istitle())  # Se começa com letra maiúscula



